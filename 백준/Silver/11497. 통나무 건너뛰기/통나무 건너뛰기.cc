using namespace std;
#define ll long long

void g(void)
{
    ios::sync_with_stdio(false);
    cin.tie(NULL);
}

int main(void)
{
    g();
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        int p = 0;
        vector<int> v(n);
        for (int i = 0; i < n; i++) {
            cin >> v[i];
        }
        sort(v.begin(), v.end());
        for (int i = 0; i < n - 2; i++) {
            p = max(p, abs(v[i] - v[i + 2]));
        }
        cout << p << "\n";
    }
}